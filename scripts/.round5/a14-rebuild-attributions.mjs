// Rebuild docs/photo-attributions.md from the current catalogue (post-swap state).
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const repoRoot = join(__dirname, "..", "..");

const catalogue = JSON.parse(
  readFileSync(join(repoRoot, "scripts", "photo-catalogue.json"), "utf8"),
);

const stockEntries = catalogue
  .filter((e) => e.source && e.source.type === "stock")
  .sort((a, b) => a.src.localeCompare(b.src));

const lines = [
  "# Stock photo attributions",
  "",
  "Reuse-allowed stock photographs sourced for trip-detail galleries.",
  "All entries are licensed under the [Pexels License](https://www.pexels.com/license/),",
  "which permits free commercial + non-commercial use; attribution is appreciated but",
  "not required. The portfolio nevertheless preserves attribution in",
  "`scripts/photo-catalogue.json` (`source` block) and in this document so that the",
  "lightbox / detail UI can surface photographer credit once that work lands.",
  "",
  "_Generated by `scripts/.round5/a14-rebuild-attributions.mjs` on " +
    new Date().toISOString().slice(0, 10) +
    "._",
  "",
  "| File | City | Photographer | Provider | Source URL |",
  "| --- | --- | --- | --- | --- |",
];

for (const e of stockEntries) {
  lines.push(
    `| \`public/photos/${e.src}\` | ${e.place.display} | [${e.source.photographer}](${e.source.photographerUrl}) | ${e.source.provider} | [link](${e.source.url}) |`,
  );
}

lines.push("");
lines.push("## License terms");
lines.push("");
lines.push("- **Pexels License** — https://www.pexels.com/license/");
lines.push("  - Free for commercial and non-commercial use.");
lines.push("  - Attribution appreciated but not required.");
lines.push("  - May not be sold without significant modification.");
lines.push("  - May not be used to depict identifiable people in a bad light.");
lines.push("");
lines.push(`Total stock photos: ${stockEntries.length}`);
lines.push("");

writeFileSync(join(repoRoot, "docs", "photo-attributions.md"), lines.join("\n"), "utf8");
console.log(`Wrote attribution doc with ${stockEntries.length} entries.`);
